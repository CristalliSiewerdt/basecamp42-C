char	*ft_strlowcase(char *str);

char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] > '@' && str[i] < '[')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}
